# TUPLE  it is used to store multiple items in a single variable.

x = ("apple", "banana", "cherry")
print(x)  # Tuple is unchangeable meaning items cannot be change, remove, or add.

# SET - Set isa collection which is unordered,unchangeable and unindexed.
thisset = {"apple", "banana", "cherry"}
print(thisset)

